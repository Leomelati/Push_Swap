#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	i;
	char	*p;

	i = 0;
	while (src[i] != '\0')
		i++;
	p = malloc((i + 1) * sizeof(char));
	if (!(p))
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		p[i] = src[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
